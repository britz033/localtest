package com.example.onlydb;

public class BusItem {
	private String busNumber;
	private String busInterval;
	private String busFoward;
	private String busBackward;
	
	public String getBusNumber() {
		return busNumber;
	}
	public void setBusNumber(String busNumber) {
		this.busNumber = busNumber;
	}
	public String getBusInterval() {
		return busInterval;
	}
	public void setBusInterval(String busInterval) {
		this.busInterval = busInterval;
	}
	public String getBusFoward() {
		return busFoward;
	}
	public void setBusFoward(String busFoward) {
		this.busFoward = busFoward;
	}
	public String getBusBackward() {
		return busBackward;
	}
	public void setBusBackward(String busBackward) {
		this.busBackward = busBackward;
	}
	
	
}
